"""
Завдання 1. Оптимізація виробництва

Компанія виробляє два види напоїв: "Лимонад" і "Фруктовий сік". 
Для виробництва цих напоїв використовуються різні інгредієнти та обмежена кількість обладнання. 
Задача полягає у максимізації виробництва, враховуючи обмежені ресурси.

Умови завдання:
"Лимонад" виготовляється з "Води", "Цукру" та "Лимонного соку".
"Фруктовий сік" виготовляється з "Фруктового пюре" та "Води".
Обмеження ресурсів: 100 од. "Води", 50 од. "Цукру", 30 од. "Лимонного соку" та 40 од. "Фруктового пюре".
Виробництво одиниці "Лимонаду" вимагає 2 од. "Води", 1 од. "Цукру" та 1 од. "Лимонного соку".
Виробництво одиниці "Фруктового соку" вимагає 2 од. "Фруктового пюре" та 1 од. "Води".

Використовуючи PuLP, створіть модель, яка визначає, скільки "Лимонаду" та "Фруктового соку" потрібно 
виробити для максимізації загальної кількості продуктів, дотримуючись обмежень на ресурси. 
Напишіть програму, код якої максимізує загальну кількість вироблених продуктів "Лимонад" та 
"Фруктовий сік", враховуючи обмеження на кількість ресурсів.
"""

import pulp

model = pulp.LpProblem("Maximize Production", pulp.LpMaximize)

limonade = pulp.LpVariable('Limonade', lowBound=0, cat='Integer')
fruit_juce = pulp.LpVariable('Juce', lowBound=0, cat='Integer')

model +=limonade + fruit_juce, "Total Production"

# Constraints
model += 2* limonade + fruit_juce <= 100, "Water"
model += limonade <= 50, "Sugar"
model += limonade <= 30, "Lemon Juce"
model += 2 * fruit_juce <= 40, "Fruite puree"

model.solve()

print(f"Limonade production: {limonade.varValue}")
print(f"Fruit juce production: {fruit_juce.varValue}")
print("Total production:", pulp.value(model.objective))

